    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        try {
            String c = txtCari.getText();
            String cari = "SELECT * FROM Kasir WHERE id_Kasir like '%%"+c+"' "+
                    "or level like '%%"+c+"' or nama like '%%"+c+"' or password like '%%"+c+"'";
            objectKu.loadDataKasir(tabelKasir, cari);
        } catch (Exception e) {
            System.out.println("form.frameKasir.txtCariKeyReleased()");
        }
    }//GEN-LAST:event_txtCariKeyReleased

    private void tabelKasirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKasirMouseClicked
        // TODO add your handling code here:
        int baris = tabelKasir.getSelectedRow();
        String id = tabelKasir.getValueAt(baris, 0).toString();
        String level = tabelKasir.getValueAt(baris, 1).toString();
        String nama = tabelKasir.getValueAt(baris, 2).toString();
        String password = tabelKasir.getValueAt(baris, 3).toString();
        
        txtKasir.setText(id);
        txtLevel.setText(level);
        txtNama.setText(nama);
        txtPassword.setText(password);
        objectKu.loadDataKasir(tabelKasir, "SELECT * FROM Kasir");
    }//GEN-LAST:event_tabelKasirMouseClicked

