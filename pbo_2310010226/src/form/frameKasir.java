    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            objectKu.simpanKasirPRT(txtKasir.getText(),
                txtLevel.getText(),
                txtNama.getText(),
                txtPassword.getText());
            if (objectKu.validasi){
                txtLevel.setText(objectKu.var_level);
                txtNama.setText(objectKu.var_nama);
                txtPassword.setText(objectKu.var_password);
            }
            objectKu.loadDataKasir(tabelKasir, "SELECT * FROM Kasir");
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
            if (
                txtKasir.getText().isEmpty() ||
                txtLevel.getText().isEmpty() ||
                txtNama.getText().isEmpty() ||
                txtPassword.getText().isEmpty()
                        ) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Terlabih Dahulu");
            tabelKasir.requestFocus();
        } else {
                objectKu.hapusKasirPRT(txtKasir.getText());
                txtKasir.setText(null);
                txtLevel.setText(null);
                txtNama.setText(null);
                txtPassword.setText(null);
                objectKu.loadDataKasir(tabelKasir, "SELECT * FROM Kasir");
        }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnHapusActionPerformed

