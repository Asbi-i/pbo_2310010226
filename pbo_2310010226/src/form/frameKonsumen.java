    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        try {
            String c = txtCari.getText();
            String cari = "SELECT * FROM konsumen WHERE id_konsumen like '%%"+c+"' "+
                    "or nama like '%%"+c+"' or alamat like '%%"+c+"' or telp like '%%"+c+"'";
            objectKu.loadDataKonsumen(tabelKonsumen, cari);
        } catch (Exception e) {
            System.out.println("form.frameKonsumen.txtCariKeyReleased()");
        }
    }//GEN-LAST:event_txtCariKeyReleased

    private void tabelKonsumenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKonsumenMouseClicked
        // TODO add your handling code here:
        int baris = tabelKonsumen.getSelectedRow();
        String id = tabelKonsumen.getValueAt(baris, 0).toString();
        String nama = tabelKonsumen.getValueAt(baris, 1).toString();
        String alamat = tabelKonsumen.getValueAt(baris, 2).toString();
        String telp = tabelKonsumen.getValueAt(baris, 3).toString();
        
        txtKonsumen.setText(id);
        txtNama.setText(nama);
        txtAlamat.setText(alamat);
        txtTelpon.setText(telp);
        objectKu.loadDataKonsumen(tabelKonsumen, "SELECT * FROM konsumen");
    }//GEN-LAST:event_tabelKonsumenMouseClicked

