    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            objectKu.simpanKonsumenPRT(txtKonsumen.getText(),
                txtNama.getText(),
                txtAlamat.getText(),
                txtTelpon.getText());
            if (objectKu.validasi){
                txtNama.setText(objectKu.var_nama);
                txtAlamat.setText(objectKu.var_alamat);
                txtTelpon.setText(objectKu.var_telpon);
            }
            objectKu.loadDataKonsumen(tabelKonsumen, "SELECT * FROM konsumen");
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

